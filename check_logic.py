import sys
from datetime import date, datetime

if hasattr(sys.stdout, "reconfigure"):        # корректный вывод кириллицы в консоль Windows
    sys.stdout.reconfigure(encoding="utf-8")

# --- Справочник «Договоры»: наименование -> (Активен, НачалоДействия) ---
C = {
    "Договор_1":  (True,  date(2025, 1, 1)),
    "Договор_2":  (False, date(2025, 1, 1)),
    "Договор_3":  (True,  date(2025, 2, 1)),
    "Договор_4":  (True,  date(2025, 2, 1)),
    "Договор_5":  (False, date(2025, 3, 1)),
    "Договор_6":  (False, date(2025, 3, 15)),
    "Договор_7":  (True,  date(2025, 3, 15)),
    "Договор_8":  (True,  date(2025, 4, 1)),
    "Договор_9":  (True,  date(2025, 5, 1)),
    "Договор_10": (False, date(2025, 6, 1)),
    "Договор_11": (False, date(2025, 7, 10)),
    "Договор_12": (False, date(2025, 8, 1)),
    # Договоры с ведущим нулём есть только в ЛР04-002048, в справочнике-примере
    # отсутствуют; трактуем как неактивные (на примеры не влияет — у ведомости
    # всё равно нет активного договора).
    "Договор_09": (False, date(2025, 5, 1)),
    "Договор_08": (False, date(2025, 4, 1)),
    "Договор_07": (False, date(2025, 3, 15)),
}

# --- Документ «Ведомость»: номер -> дата ---
V_DATE = {
    "БТ02-000016": datetime(2025, 4, 1, 8, 16, 32),
    "БТ02-000032": datetime(2025, 5, 2, 16, 0, 4),
    "БТ02-000064": datetime(2025, 5, 16, 4, 2, 1),
    "БТ02-000128": datetime(2025, 6, 1, 0, 32, 0),
    "ЛР04-000256": datetime(2025, 4, 10, 2, 2, 2),
    "ЛР04-000512": datetime(2025, 4, 20, 16, 4, 8),
    "ЛР04-001024": datetime(2025, 5, 12, 1, 1, 16),
    "ЛР04-002048": datetime(2025, 8, 1, 8, 8, 4),
}

# --- Документ «Ведомость». Табличная часть «Договоры» (в порядке НомерСтроки) ---
V_ROWS = {
    "БТ02-000016": ["Договор_1", "Договор_2"],
    "БТ02-000032": ["Договор_4", "Договор_5", "Договор_3", "Договор_7"],
    "БТ02-000128": ["Договор_7", "Договор_8", "Договор_9"],
    "ЛР04-000256": ["Договор_1", "Договор_3", "Договор_2", "Договор_8",
                    "Договор_7", "Договор_5", "Договор_6", "Договор_4"],
    "ЛР04-000512": ["Договор_12"],
    "ЛР04-001024": ["Договор_3", "Договор_11"],
    "ЛР04-002048": ["Договор_09", "Договор_10", "Договор_11", "Договор_08", "Договор_07"],
}

# --- Регистр «ИсторияСтатусовДокументов»: номер -> [(ДатаЗаписи, Статус)] ---
V_HIST = {
    "БТ02-000016": [(datetime(2025, 4, 1, 10, 10, 10), "Создан"),
                    (datetime(2025, 4, 1, 11, 11, 11), "Заблокирован"),
                    (datetime(2025, 5, 19, 12, 12, 12), "Акцептован")],
    "БТ02-000032": [(datetime(2025, 5, 2, 16, 0, 16), "Внутренний")],
    "БТ02-000064": [(datetime(2025, 5, 19, 12, 12, 12), "Акцептован"),
                    (datetime(2025, 5, 17, 17, 18, 19), "Заблокирован")],
    "БТ02-000128": [(datetime(2025, 6, 1, 1, 11, 10), "Акцептован"),
                    (datetime(2025, 6, 1, 1, 23, 45), "Внутренний")],
    "ЛР04-000256": [(datetime(2025, 4, 11, 10, 4, 26), "Заблокирован"),
                    (datetime(2025, 4, 10, 10, 4, 25), "Создан"),
                    (datetime(2025, 4, 10, 14, 0, 41), "Заблокирован"),
                    (datetime(2025, 4, 11, 10, 4, 25), "Акцептован")],
    "ЛР04-001024": [(datetime(2025, 5, 12, 2, 2, 32), "Создан"),
                    (datetime(2025, 5, 12, 20, 20, 23), "Акцептован")],
    "ЛР04-002048": [(datetime(2025, 8, 2, 23, 59, 59), "Заблокирован"),
                    (datetime(2025, 8, 2, 0, 0, 0), "Создан"),
                    (datetime(2025, 8, 3, 12, 0, 0), "Внутренний")],
}


def first_active_date(num):
    """Дата начала действия ПЕРВОГО активного договора (по порядку строк ТЧ)."""
    for name in V_ROWS[num]:
        if C[name][0]:
            return C[name][1]
    return None


def all_dates(num):
    return [C[name][1] for name in V_ROWS[num]]


def last_status(num):
    """Последний статус = статус записи с максимальной ДатаЗаписи."""
    hist = V_HIST.get(num, [])
    return max(hist, key=lambda r: r[0])[1] if hist else None


def fill(extremum="min", relation="eq", exclude_by_day=False, today=None):
    """Команда «Заполнить нужное»."""
    result = []
    for num in V_ROWS:
        fa = first_active_date(num)
        if fa is None:                       # нет активного договора — пропускаем
            continue
        dates = all_dates(num)
        ref = min(dates) if extremum == "min" else max(dates)
        matches = (fa == ref)
        if relation == "ne":
            matches = not matches
        if not matches:
            continue
        # (1) индивидуальное исключение (диапазон дня отправки 1–10):
        #     исключить, если день даты ведомости = дню текущей даты.
        if exclude_by_day and V_DATE[num].day == today.day:
            continue
        result.append(num)
    result.sort(key=lambda n: V_DATE[n])     # по возрастанию даты документа
    return result


def delete_even(table):
    """Команда «Удалить лишнее»: убрать ведомости с чётным числом договоров."""
    kept, deleted = [], []
    for num in table:
        (deleted if len(V_ROWS[num]) % 2 == 0 else kept).append(num)
    deleted.sort()                           # по возрастанию номера ведомости
    n = len(deleted)
    msg = (f"Удалено документов: {n} ({', '.join(deleted)})." if n
           else "Удалено документов: 0.")
    return kept, msg


def green_rows(table):
    return {num for num in table if last_status(num) == "Акцептован"}


# ---------------- Пример 1: «равна» + «минимальной» ----------------
t1 = fill(extremum="min", relation="eq")
assert t1 == ["БТ02-000016", "ЛР04-000256", "БТ02-000032",
              "ЛР04-001024", "БТ02-000128"], t1
assert green_rows(t1) == {"БТ02-000016", "ЛР04-001024"}, green_rows(t1)
kept1, msg1 = delete_even(t1)
assert kept1 == ["БТ02-000128"], kept1
# NB: в PDF здесь опечатка — напечатано «БТ02-000256» вместо «ЛР04-000256».
assert msg1 == ("Удалено документов: 4 (БТ02-000016, БТ02-000032, "
                "ЛР04-000256, ЛР04-001024)."), msg1

# ---------------- Пример 2: «равна» + «максимальной» ----------------
t2 = fill(extremum="max", relation="eq")
assert t2 == ["БТ02-000016"], t2
kept2, msg2 = delete_even(t2)
assert kept2 == [], kept2
assert msg2 == "Удалено документов: 1 (БТ02-000016).", msg2

print("OK — логика совпадает с Примером 1 и Примером 2 из задания.")
print("Пример 1 → таблица:", t1)
print("Пример 1 → зелёные:", sorted(green_rows(t1)))
print("Пример 1 → удаление:", msg1)
print("   (в PDF в этом месте опечатка: 'БТ02-000256' должно быть 'ЛР04-000256')")
print("Пример 2 → удаление:", msg2)
